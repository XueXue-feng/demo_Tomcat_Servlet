package com.sample;


public class RollClients {

  private long id;
  private String clientsName;
  private String clientsImage;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getClientsName() {
    return clientsName;
  }

  public void setClientsName(String clientsName) {
    this.clientsName = clientsName;
  }


  public String getClientsImage() {
    return clientsImage;
  }

  public void setClientsImage(String clientsImage) {
    this.clientsImage = clientsImage;
  }

}
